            <div class="sign-up">
                <div class="username">
                    <i class="lni lni-user"></i>
                    <input type="username" name="username" placeholder="username" <?php echo (!empty($username_err)) ? 'is-invalid' : ''; ?>" value="<?php echo $username; ?>">
                    <span class="invalid-feedback"><?php echo $username_err; ?></span>
                </div>
                <div class="password">
                    <i class="lni lni-lock"></i>
                    <input type="password" name="password" placeholder="password" <?php echo (!empty($password_err)) ? 'is-invalid' : ''; ?>" value="<?php echo $password; ?>">
                    <span class="invalid-feedback"><?php echo $password_err; ?></span>
                </div>
                <div class="password">
                    <i class="lni lni-lock"></i>
                    <input type="password" name="confirm_password" placeholder="confirm-password" <?php echo (!empty($confirm_password_err)) ? 'is-invalid' : ''; ?>" value="<?php echo $confirm_password; ?>">
                    <span class="invalid-feedback"><?php echo $confirm_password_err; ?></span>
                </div>
            </div>
            
            <button input type="submit" class="buttons" value="Submit">Sign Up </button>
            <div class="link">
                <a href="login.php">Already have an account?</a> or
                <a href="login.php">Login</a>
            </div>
